package StreamEx;

public class Employee {
	String name;
	int marks;
	
	public Employee(String name,int marks) {
		this.name=name;
		this.marks=marks;
		
	}
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getMarks() {
		return marks;
	}

	public void setMarks(int marks) {
		this.marks = marks;
	}

}

